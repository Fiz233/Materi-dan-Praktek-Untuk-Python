# disini ada for loop
# dimana gunanya untuk mengulang suatu kondisi yang sesuai dengan yang diperintah
# jadi dibandingkan kalian menulis perintah print banyak2, mending gunakan ini aja

#contoh

for i in range (5):     # nah nanti dibakal tulis dari 0 sampai 4
    print(i)            # nah diingat lagi kalau indexnya itu di itung dari 
                        # dan nomernya dimulai dan sebelum yang kalian inginkan
                        # berati kalau range nya 5, maka akan berhenti ketika sudah menyentuh nomor 5

for i in range (2, 6):  # nah kalau ini kalau mau dari angka berapa ke berapa yang kita inginkan
    print(i)            # intinya (angka dimulai, angka yang berhentinya)

for i in range (2, 7, 2): # nah yang ini diangka terakhirnya itu buat kelipatannya
    print(i)