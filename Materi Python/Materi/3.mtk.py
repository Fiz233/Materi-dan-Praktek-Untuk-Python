# ada macam2 nih

angka1 = 20
angka2 = 40

pertambahan = (angka1 + angka2) # ini bua nambah tapi yaudahlah pada ngerti sisanya
pengurangan = (angka1 - angka2)
pembagian = (angka1 // angka2) # nah disini bisa aja lu make nya "/" itu untuk yang ada desimal nya, kalau yang dobel itu bilangan yang bulat
perkalian = (angka1 * angka2) # jangan pake yang huruf X ya ges, gak bisa itu mah
modulus = (angka1 % angka2) # pokoknya dibagi sampe abis lah ya

print(pertambahan, pengurangan, pembagian, perkalian, modulus)