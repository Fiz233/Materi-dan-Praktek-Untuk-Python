# nah disini kek biasa aja sih, perbandingan dalam MTK
# == itu sama dengan
# <= itu kurang dari sama dengan 
# >= 
# < >
# != itu tidak sama dengan

a = 10
b = 5

print(a == b)   # False
print(a != b)   # True
print(a > b)    # True
print(a < b)    # False
print(a >= 10)  # True
print(b <= 5)   # True